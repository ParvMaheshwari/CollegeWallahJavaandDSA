/*
==========================
   JAVA OPERATORS NOTES
==========================

1. Arithmetic Operators
   +  Addition        → a + b        | When: Basic math operations | How: int sum = a + b;
   -  Subtraction     → a - b        | When: Difference of values  | How: int diff = a - b;
   *  Multiplication  → a * b        | When: Product calculation   | How: int prod = a * b;
   /  Division        → a / b        | When: Quotient (int division)| How: int div = a / b;
   %  Modulus         → a % b        | When: Remainder needed      | How: int rem = a % b;

2. Unary Operators
   +   Positive sign                 | When: Explicit positive     | How: +x;
   -   Negative sign                 | When: Negate value          | How: -x;
   ++  Increment (pre/post)          | When: Increase by 1         | How: ++x / x++;
   --  Decrement (pre/post)          | When: Decrease by 1         | How: --x / x--;
   !   Logical NOT                   | When: Invert boolean        | How: !flag;

3. Assignment Operators
   =   Assign value                  | When: Initialize/update var | How: x = 10;
   +=  Add and assign                | When: Add & store result    | How: x += 5;  | a = a + 5;
   -=  Subtract and assign           | When: Subtract & store      | How: x -= 3;  | a = a - 3;
   *=  Multiply and assign           | When: Multiply & store      | How: x *= 2;  | a = a * 2;
   /=  Divide and assign             | When: Divide & store        | How: x /= 4;  | a = a / 4;
   %=  Modulus and assign            | When: Remainder & store     | How: x %= 2;  | a = a % 2;

4. Relational Operators (Ya comparison ka results ko evaluate karna mein use hota hai.)
   ==  Equal to                      | When: Compare equality      | How: if(a == b)
   !=  Not equal to                  | When: Compare inequality    | How: if(a != b)
   >   Greater than                  | When: Check larger value    | How: if(a > b)
   <   Less than                     | When: Check smaller value   | How: if(a < b)
   >=  Greater or equal              | When: Larger or equal check | How: if(a >= b)
   <=  Less or equal                 | When: Smaller or equal check| How: if(a <= b)

5. Logical Operators
   &&  Logical AND                   | When: Both conditions true  | How: if(a>0 && b>0)
   ||  Logical OR                    | When: At least one true     | How: if(a>0 || b>0)
   !   Logical NOT                   | When: Invert condition      | How: if(!flag)

6. Bitwise Operators
   &   Bitwise AND                   | When: Bit-level AND         | How: a & b
   |   Bitwise OR                    | When: Bit-level OR          | How: a | b
   ^   Bitwise XOR                   | When: Bit-level XOR         | How: a ^ b
   ~   Bitwise Complement            | When: Flip bits             | How: ~a
   <<  Left shift                    | When: Multiply by 2^n       | How: a << 2
   >>  Right shift                   | When: Divide by 2^n         | How: a >> 1
   >>> Unsigned right shift          | When: Shift with 0 fill     | How: a >>> 1

7. Ternary Operator
   condition ? value_if_true : value_if_false
   | When: Short if-else             | How: int max = (a>b)?a:b;

8. instanceof Operator
   obj instanceof ClassName
   | When: Check object type         | How: if(obj instanceof String)

9. Type Casting
   (type) value → e.g. (int) 3.14
   | When: Convert data type         | How: int i = (int) 3.14;
*/




